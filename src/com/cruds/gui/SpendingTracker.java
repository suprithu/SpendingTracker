    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int sr = table1.getSelectedRow();
        if(sr != -1)
        {
            int result = JOptionPane.showConfirmDialog(null, "Do you really want to delete selected Item.", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION)
           {
                int id = (int)table1.getValueAt(sr, 0);
                SpendingDao dao = new SpendingDao();
                dao.deleteData(id);

                getEntries();
            }
                    
        }
    }//GEN-LAST:event_jButton3ActionPerformed

