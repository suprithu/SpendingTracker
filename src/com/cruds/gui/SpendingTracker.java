    private void getEntries()
    {
        javax.swing.table.DefaultTableModel dt = (javax.swing.table.DefaultTableModel)table1.getModel();
         
        int rowCount = dt.getRowCount();
        while(rowCount-- != 0)
        {
            dt.removeRow(0);
        }
         SpendingDao dao = new SpendingDao();
         dao.getAll(dt);
         int ttl = dao.getTotal();
         total.setText(ttl+" ");
    }

